#include <stdio.h>

int main()
{
    printf("Hello, Java!\n\n");

    int i = 0;
    while (i < 5)
    {
        printf("Hello, Java!\n");
        i++;
    }


    // calculator
    printf("\n");
    
    int k = 0;
    while (k <= 10)
    {
        printf("%d * 6 = %d\n", k, k * 6);
        k++;
    }
    
    return 0;
}